package variable;

public class Var5 {
    public static void main(String[] args) {
        // 변수 선언, 초기화 각각 따로
        int a;
        a = 10;
        System.out.println(a);

        // 변수 선언과 초기화를 한번에
        int b = 10;
        System.out.println(b);

        // 여러 변수 한꺼번에 초기화
        int c = 1, d= 6;
        System.out.println(c);
        System.out.println(d);
    }
}
